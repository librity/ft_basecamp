git ls-files --other --ignored --exclude-standard
# --ignored: Mostra todos os arquivos commitados

# --other: Mostra todos os arquivos nao commitados
# dos nao commitados, mostra os arquivos iginorados

# --exclude-standard: Puxa as regras de ignorar arquivos dos locais padroes:
# .gitgnore, .git/info/exclude e o arquivo de exclusão global do usuário
