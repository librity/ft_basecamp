#include <unistd.h>

void	put_char(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char letter;

	letter = 'z';
	while (letter >= 'a')
	{
		put_char(letter);
		letter--;
	}
}
