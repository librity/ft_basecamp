#include <unistd.h>

void	put_char(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char letter;

	letter = 'a';
	while (letter <= 'z')
	{
		put_char(letter);
		letter++;
	}
}
